
public final class Calculator {
	
	// PERFECT method
	public static int add(int a, int b)
	{
		int sum=a+b;
		return sum;
	}
	// PERFECT method
	public static int sub(int a, int b)
	{
		int sub=a-b;
		return sub;
	}
	// PERFECT method
	public static int mul(int a, int b)
	{
		int product=a*b;
		return product;
	}
}
